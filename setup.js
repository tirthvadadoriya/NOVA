  console.log('ğŸš€ NOVA AI Chatbot Setup');
  console.log('========================\n');
  
  const envPath = path.join(__dirname, '.env');
  
  // Check if .env already exists
  if (fs.existsSync(envPath)) {
    const overwrite = await question('âš ï¸  .env file already exists. Overwrite? (y/N): ');
    if (overwrite.toLowerCase() !== 'y' && overwrite.toLowerCase() !== 'yes') {
      console.log('Setup cancelled.');
      rl.close();
      return;
    }
  }
  
  console.log('\nğŸ“ Let\'s configure your API keys:');
  console.log('(Press Enter to skip optional fields)\n');
  
